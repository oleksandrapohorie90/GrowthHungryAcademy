package Github_200days.numbers;

public class MathStuff {
    public static void main(String[] args) {
        System.out.println((double)7/4);
        System.out.println((double)7/4);
        //whichever num has a higher level of precision
        //higher number put f to let JVM know you want a number to have decimal point
        System.out.println(2+3*5); //multiplication is first then addition

        System.out.println(4%2);
        //modulo - %, regular division + returns the remainder
        int x = 1;
        //--x
        System.out.println(x); //it is zero
        //x++
        System.out.println(x);//still 1

        boolean myFlag = true;
        System.out.println(!myFlag);

    }
}
